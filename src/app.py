def add(a, b):
    """
    Function to add two numbers.
    Args:
        a (int or float): First number.
        b (int or float): Second number.
    Returns:
        int or float: Sum of the two numbers.
    """
    return a + b

if __name__ == "__main__":
    print(f"3 + 5 = {add(3, 5)}")
